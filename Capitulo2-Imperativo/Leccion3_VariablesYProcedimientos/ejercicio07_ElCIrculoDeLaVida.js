/*
Definí las funciones perimetroCirculo y areaCirculo que reciben el 
radio de un círculo y a partir del mismo nos retornan su perímetro 
y su área.
*/

function perimetroCirculo(radio){
	return((2*3.14159265358979) * radio);
}

function areaCirculo(radio){
	return(3.14159265358979 * (radio*radio));
}